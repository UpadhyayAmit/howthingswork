"use client";

import { useState, useCallback } from "react";
import { motion, AnimatePresence } from "framer-motion";
import Button from "@/app/_ui/Button";
import Panel from "@/app/_ui/Panel";

type Mode = "reflection" | "sourcegen";

interface PropertyInfo {
  name: string;
  typeName: string;
  attributes: string[];
  canRead: boolean;
  canWrite: boolean;
}

interface MethodInfo {
  name: string;
  returnType: string;
  parameters: string;
  isPublic: boolean;
  isAsync: boolean;
}

interface TypeData {
  fullName: string;
  baseType: string;
  interfaces: string[];
  isClass: boolean;
  properties: PropertyInfo[];
  methods: MethodInfo[];
  attributes: string[];
  sourceGenCode: string;
}

